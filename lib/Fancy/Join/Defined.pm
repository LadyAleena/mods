package Fancy::Join::Defined;
use v5.10.0;
use strict;
use warnings;
use Exporter qw(import);

our $VERSION   = '1.0';
our @EXPORT_OK = qw(join_defined);

sub join_defined {
  my $joiner = shift @_;
  return join($joiner, grep(defined, @_));
}

